void CCMCalibrationPageWidget::loadLscData(int idx)
{
    lsc_imported = false;
    if (rawItem.isEmpty() || rawItem.size() < idx) {
        return;
    }
    QStringList lights = {"D75", "D65", "D50", "TL84", "A"};
    QString filepath = rawItem[idx]->getFilePath();
    QString filename = rawItem[idx]->getFileName();
    QStringList strList = filepath.split("/");
    int size = strList.size();
    int idxL;
    for (idxL=0; idxL<lights.size(); idxL++) {
        if (strList[size-2].contains(lights[idxL])) {
            break;
        }
    }

    if (idxL >= lights.size()) {
        return;
    }

    QString curpath = filepath.remove(filename);
    QString lscpath = curpath + "../../LSC";
    QDir lscdir(lscpath);
    QFileInfoList folder_list = lscdir.entryInfoList(QDir::Dirs | QDir::NoDotAndDotDot);
    QString ctdir;
    for (int i=0; i<folder_list.size(); i++) {
        QString dir_name = folder_list.at(i).fileName();
        if (dir_name.contains(lights[idxL])) {
            ctdir = dir_name;
            break;
        }
    }

    if (ctdir.isEmpty()) {
        return;
    }

    QStringList fileList;
    utils::findFiles(lscpath + "/" + ctdir, ".json", fileList, true);
    for (int i=0; i<fileList.size(); i++) {
        QFileInfo info(fileList[i]);
        if (info.fileName().contains("lsc")) {
            QJsonObject data;
            QString error_msg;

            if (utils::loadJson(fileList[i], data, error_msg) != 0) {
                return;
            }

            handleImportData(fileList[i], data);
            break;
        }
    }
}

void CCMCalibrationPageWidget::HandleCalibrationRequst(QJsonObject params, RawConfigItem config)
{
    qDebug("CCMCalibrationPageWidget::HandleCalibrationRequst");
    QString path;
    int ret = 0;
    if (params.contains("path") && !(params["path"].toString().isEmpty())) {
        path = params["path"].toString();
    }

    if (path.isEmpty()) {
        emit sigCaliStatus(CALIBRATION_MODULE_ID::CCM, CALIBRATION_STATUS::CAL_ERROR, "Image path is empty!");
        return;
    }

    QDir dir(path);
    if (!dir.exists()) {
        emit sigCaliStatus(CALIBRATION_MODULE_ID::CCM, CALIBRATION_STATUS::CAL_ERROR, "Image path not exists!");
        return;
    }

    if (cali_status == CALIBRATION_STATUS::CALIBRATION) {
        emit sigCaliStatus(CALIBRATION_MODULE_ID::CCM, CALIBRATION_STATUS::CAL_ERROR, "Calibration is busy!");
        return;
    }

    if (params.contains("strength")) {
        double strength = params["strength"].toDouble();
        strength_slider->setData(strength);
    }

    if (params.contains("gamma type")) {
        int type = params["gamma type"].toInt();
        isp_gamma_item->setCurrentIndex(type);
        if (type == 2) {
            QJsonArray arr = params["gamma"].toArray();
            for (int i = 0; i < arr.size(); i++) {
                isp_gamma[i] = arr[i].toInt();
            }
        }
    }

    if (params.contains("lab type")) {
        int type = params["lab type"].toInt();
        lab_ref_item->setCurrentIndex(type);
    }

    if (params.contains("weight")) {
        QJsonArray arr = params["weight"].toArray();
        for (int i = 0; i < PATCH_WEIGHT_ROW; i++) {
            for (int j = 0; j < PATCH_WEIGHT_COL; j++) {
                int value = arr[i * PATCH_WEIGHT_ROW + j].toInt();
                weight_table->item(i, j)->setText(QString::number(value));
            }
        }
    }

    this->config = config;

    ret = QuickCalibrationLoadData(path);
    if (ret < 0) {
        emit sigCaliStatus(CALIBRATION_MODULE_ID::CCM, CALIBRATION_STATUS::CAL_ERROR, "Load file fail!");
        return;
    }

    ret = handleQuickCalibration();
    if (ret == -1) {
        emit sigCaliStatus(CALIBRATION_MODULE_ID::CCM, CALIBRATION_STATUS::CAL_ERROR, "Calibration fail!");
        return;
    } else if (ret == -2) {
        emit sigCaliStatus(CALIBRATION_MODULE_ID::CCM, CALIBRATION_STATUS::WARNING, "Write data fail!");
        return;
    }

    emit sigCaliStatus(CALIBRATION_MODULE_ID::CCM, CALIBRATION_STATUS::READY, "Calibration finish!");
}
