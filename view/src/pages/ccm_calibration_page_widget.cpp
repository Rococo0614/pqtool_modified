void CCMCalibrationPageWidget::ClearData()
{
    for (int i = 0; i < rawItem.size(); i++) {
        if (rawItem[i]) {
            delete rawItem[i];
        }
    }
    rawItem.clear();
    for (int i = 0; i < ct_item_list.size(); i++) {
        if (ct_item_list[i]) {
            delete ct_item_list[i];
        }
    }
    ct_item_list.clear();
    raw_img_item_vec.clear();
    cali_status = CALIBRATION_STATUS::READY;
    quickMode = false;
    blc_imported = false;
    lsc_imported = false;
}

