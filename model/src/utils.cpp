ushort encode_12_10_12 (bool nop, ushort xorig, ushort xpred)
{
	ushort   xenco ;
	short   xdiff = xorig - xpred;
	bool  s     = get_sign(xdiff);
	ushort   xabs  = abs(xdiff);

	if      (nop)        xenco = fix_zero(xorig >> 2);
	else if (xabs < 128) xenco =            (s << 7) |   xabs         ;
	else if (xabs < 256) xenco = (2 << 7) | (s << 6) | ((xabs-128)>>1);
	else if (xabs < 512) xenco = (3 << 7) | (s << 6) | ((xabs-256)>>2);
	else                 xenco = (1 << 9) | (xorig >> 3);
	return xenco;
}

