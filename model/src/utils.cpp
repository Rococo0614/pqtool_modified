QVariant generateDciGammaCurve(double max_x, double max_y, ItemValueType params)
{
    QVariantList data;
    double offset = params["DciOffset"].toDouble();
    double gamma = params["DciGamma"].toDouble();
    double contrast = params["DciContrast"].toDouble();
    int method = 1;

    if (params.contains("Method")) {
        method = params["Method"].toDouble();
    }


    double of = offset / 100.0;
    double ga = gamma / 10.0;
    double co = contrast;
    max_y = 255;
    max_x = 256;

    for (int i = 0; i < max_x; i++) {
        double fg, fc;
        double y;
        double p = 4 * pow(of / (1 + of), ga);
        double pout = max_y * ((1 + of) * pow(p, 1 / ga) - of);

        if ((double)i / (max_x - 1) < p) {
            fg = pout * i / (p * (max_x));
        } else {
            fg = max_y * ((1 + of) * pow((double)i / (max_x - 1), 1 / ga) - of);
        }

        fc = fg - (1 + (co - 1) * 15) * sin(2 * PI * fg / 256);

        if (method) {
            y = fc + 0.5;
            y = qBound(0.0, y, max_y);
        } else {
            y = fc * 4 + 0.5;
            y = qBound(0.0, y, 1023.0);
        }

        data.append(y);

        if (i == max_x - 1) {
            data.append(y);
        }
    }

    return data;
}

