std::vector<unique_ptr<int []>> utils::readMultiRawIntData(QString rawfilename, int width, int height)
{
    std::vector<unique_ptr<int []>> image_vec;

    unsigned int size = static_cast<unsigned int>(width) * static_cast<unsigned int>(height) *
            static_cast<unsigned int>(sizeof(short));
    QFile file(rawfilename);
    file.open(QIODevice::ReadOnly);
    QByteArray data = file.readAll();
    file.close();
    unsigned int img_cnt = static_cast<unsigned int>(data.size()) / size;

    // convert to int layout
    for (int i = 0; i < img_cnt; i++) {
        unique_ptr<int []> image(new int [size]);
        int idx = 0;
        for (int r = 0; r < height; r ++ ){
            for (int c = 0; c < width; c ++ ){
                if (idx < static_cast<int>(size)) {
                    unsigned char b1 = data.at(i*size + idx);
                    unsigned char b2 = data.at(i*size + idx + 1);
                    idx += 2;
                    image.get()[(r * width) + c] = static_cast<int>(b2 << 8 | b1);
                }
            }
        }
        image_vec.push_back(std::move(image));
    }

    return image_vec;
}

