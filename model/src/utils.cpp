QList<QImage> utils::spliceGrayImage(QList<QImage> &image_list)
{
    QList<QImage> image_result_list;
    QImage image = image_list.at(0);
    int image_num = image_list.size();
    int image_wide = image.width(), image_height = image.height();
    cv::Size spliceSize(image_wide * 2, image_height);
    cv::Mat frame_gray;
    cv::Mat calibImages[image_num];

    if (image_num % 2 != 0) {
        qDebug("image_num % 2 != 0, value: %d image_num: %d", image_num % 2, image_num);
        return image_list;
    }

    for (int i = 0; i < image_num; i++) {
        QImage image = image_list.at(i);

        frame_gray = cv::Mat(image_height, image_wide, CV_8UC1, (void*)image.constBits(), image.bytesPerLine());
        frame_gray.copyTo(calibImages[i]);
    }

    for (int i = 0; i < image_num / 2; i++) {
        QImage result_img;
        cv::Mat spliceImage = cv::Mat::zeros(spliceSize, CV_8UC1);

        calibImages[i * 2].copyTo(spliceImage(cv::Rect(0, 0, image_wide, image_height)));
        calibImages[i * 2 + 1].copyTo(spliceImage(cv::Rect(image_wide, 0, image_wide, image_height)));
        result_img = utils::cvMat2QImage(spliceImage);
        image_result_list.append(result_img);
    }

    return image_result_list;
}

