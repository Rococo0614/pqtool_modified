void flattenStructMember(QString &struct_id, QJsonObject &all_struct_obj, QString &prefix_id, QString &prefix_alias,
    QJsonArray &flat_member_array, QJsonObject &mapping_obj, QJsonArray range_array, QString access_str)
{
    QJsonObject struct_obj = all_struct_obj[struct_id].toObject();
    QJsonArray member_array = struct_obj["MEMBER"].toArray();
    int member_array_count = 0;
    bool bIsNoiseCalCoef = false;

    for (int i = 0; i < member_array.count(); i++) {
        QJsonObject member_obj = member_array.at(i).toObject();

        if ((prefix_id == QString("ISP_CMOS_NOISE_CALIBRATION_S.")) && (member_obj["ID"].toString() == QString("CalibrationCoef"))) {
            QJsonValue count_value = member_obj["COUNT"];
            QJsonArray temp_array = count_value.toArray();
            member_array_count = temp_array[1].toInt() * temp_array[2].toInt();
            bIsNoiseCalCoef = true;
            break;
        }
    }

    if (member_array_count == 0) {
        member_array_count = member_array.count();
    }

    for (int i = 0; i < member_array_count; i++) {
        QJsonObject member_obj;
        if (bIsNoiseCalCoef) {
            member_obj = member_array.at(0).toObject();
        } else {
            member_obj = member_array.at(i).toObject();
        }
        QString type_str = member_obj["TYPE"].toString();
        if (all_struct_obj.contains(type_str)) {
            // for ISP_EXPOSURE_ATTR_S's max and min ranges setting
            QJsonArray range_array;
            if (member_obj.contains("RANGE"))
                range_array = member_obj["RANGE"].toArray();

            // for struct members readonly setting
            QString access_str;
            if (member_obj.contains("ACCESS"))
                if (member_obj["ACCESS"] == "R")
                    access_str = "R";

            if (!member_obj.contains("COUNT")) {
                QString path_str = prefix_id + member_obj["ID"].toString() + '.';
                QString alias_str = prefix_alias + member_obj["ALIAS"].toString() + '.';
                flattenStructMember(type_str, all_struct_obj, path_str, alias_str, flat_member_array, mapping_obj, range_array, access_str);
                if (type_str.left(4) == "PQT_") {
                    mapping_obj.insert(prefix_id + member_obj["ID"].toString(), type_str);
                }
            } else {
                for (int j = 0; j < member_obj["COUNT"].toInt(); j++) {
                    QString path_str = prefix_id + member_obj["ID"].toString() + QString("[%1].").arg(j);
                    QString alias_str = prefix_alias + member_obj["ALIAS"].toString() + QString("[%1].").arg(j);
                    flattenStructMember(type_str, all_struct_obj, path_str, alias_str, flat_member_array, mapping_obj, range_array, access_str);
                }
            }
        } else {
            member_obj["ID"] = prefix_id + member_obj["ID"].toString();
            member_obj["ALIAS"] = prefix_alias + member_obj["ALIAS"].toString();
            if (!range_array.isEmpty()) {
                member_obj["RANGE"] = range_array;
                qDebug() << member_obj["ID"].toString() << " range is changed.";
            }

            if (!access_str.isEmpty()) {
                member_obj["ACCESS"] = access_str;
                qDebug() << member_obj["ID"].toString() << " accesss is changed.";
            }

            flat_member_array.append(member_obj);
        }
    }
}

int createWidgetInfo(QString struct_id, QJsonObject &member_obj, QJsonObject &all_enum_obj, QJsonObject &widget_obj, bool display_prefix)
{
    // get info from tool json
    QString type_str = member_obj["TYPE"].toString();
    QString desp_str = member_obj["COMMENT"].toString();
    QString id_str = member_obj["ID"].toString();
    QString alias_str = member_obj["ALIAS"].toString();
    QString access_str = member_obj["ACCESS"].toString();
    id_str = id_str.remove(struct_id + '.');
    QString path_str = struct_id + '.' + id_str;
    QJsonArray range_array =  member_obj["RANGE"].toArray();
    int widget_type = -1;
    bool bIsNoiseCalCoef = false;
    bool bIsCCMSumMember = false;

    if (member_obj["ID"].toString() == QString("ISP_CMOS_NOISE_CALIBRATION_S.CalibrationCoef")) {
        bIsNoiseCalCoef = true;
        path_str = struct_id + '.' + alias_str;
    }

    if (access_str == 'R') {
        widget_obj.insert("chmod", "ReadOnly");
    }

    if (member_obj.contains("LEVEL")) {
        widget_obj.insert("level", member_obj["LEVEL"].toInt());
    }

    if (!member_obj.contains("COUNT")) {
        if (all_enum_obj.contains(type_str)) {
            widget_type = REGISTER_TYPE::COMBOBOX;
        } else if (type_str == "CVI_BOOL") {
            widget_type = REGISTER_TYPE::BINARY;
        } else if (type_str == "CVI_U8" || type_str == "CVI_U16" || type_str == "CVI_S8"
            || type_str == "CVI_S16" || type_str == "CVI_S32") {
            widget_type = REGISTER_TYPE::SPINNER;
        } else if (type_str == "CVI_U32") {
            widget_type = REGISTER_TYPE::UINT_SPINNER;
        } else if (type_str == "CVI_FLOAT") {
            widget_type = REGISTER_TYPE::DOUBLE_SPINNER;
        } else {
            qDebug() << "[" + struct_id + '.' + id_str + "] Can't find suitable widget type.";
            return -1;
        }
    } else {
        widget_type = REGISTER_TYPE::UNIV_MATRIX;
    }

    if (id_str.contains(MSG_KEY_STMANUAL)) {
        alias_str.remove("Manual.");
        if (display_prefix)
            widget_obj.insert("optype", "Manual");
    } else if (id_str.contains(MSG_KEY_STAUTO)) {
        alias_str.remove("Auto.");
        if (display_prefix)
            widget_obj.insert("optype", "Auto");
    }

    // create ui json
    widget_obj.insert("title", alias_str);
    widget_obj.insert("key", path_str);
    widget_obj.insert("desp", desp_str);

    switch (widget_type)
    {
        case REGISTER_TYPE::BINARY:
            widget_obj.insert("type", "binary");
            break;
        case REGISTER_TYPE::COMBOBOX: {
            widget_obj.insert("type", "combobox");
            QJsonObject enum_obj = all_enum_obj[type_str].toObject();
            QJsonArray enum_item_array = enum_obj["ENUM_ITEM"].toArray();
            QJsonArray param_array;
            for (int i = 0, val = 0; i < enum_item_array.count(); i++) {
                QJsonObject enum_item_obj = enum_item_array.at(i).toObject();
                if (enum_item_obj.contains("VALUE")) {
                    val = enum_item_obj["VALUE"].toInt();
                }

                QJsonObject param_obj;
                param_obj.insert("label", enum_item_obj["ID"].toString());
                param_obj.insert("value", val++);
                param_array.append(param_obj);
            }

            widget_obj.insert("param", param_array);
            break;
        }
        case REGISTER_TYPE::SPINNER:
            widget_obj.insert("type", "spinner");
            if (range_array.count() == 2 && range_array.at(0).isDouble() && range_array.at(1).isDouble()) {
                QJsonObject param_obj;
                param_obj.insert("min", range_array.at(0));
                param_obj.insert("max", range_array.at(1));
                widget_obj.insert("param", param_obj);
            } else {
                qDebug() << "[" + widget_obj["key"].toString() + "] Range definition is wrong.";
                return -2;
            }
            break;
        case REGISTER_TYPE::UINT_SPINNER:
            widget_obj.insert("type", "spinner");
            if (range_array.count() == 2 && range_array.at(0).isDouble() && range_array.at(1).isDouble()) {
                QJsonObject param_obj;
                QVariant max = range_array.at(1).toVariant();
                param_obj.insert("min", range_array.at(0));
                if (max.toUInt() >= 2147483647)
                    param_obj.insert("max", 2147483647);
                else
                    param_obj.insert("max", max.toInt());

                widget_obj.insert("param", param_obj);
            } else {
                qDebug() << "[" + widget_obj["key"].toString() + "] Range definition is wrong.";
                return -2;
            }
            break;
        case REGISTER_TYPE::DOUBLE_SPINNER:
            widget_obj.insert("type", "double_spinner");
            if (range_array.count() == 2 && range_array.at(0).isDouble() && range_array.at(1).isDouble()) {
                QJsonObject param_obj;
                param_obj.insert("min", range_array.at(0));
                param_obj.insert("max", range_array.at(1));
                widget_obj.insert("param", param_obj);
                widget_obj.insert("value_type", "double");
            } else {
                qDebug() << "[" + widget_obj["key"].toString() + "] Range definition is wrong.";
                return -2;
            }
            break;
        case REGISTER_TYPE::UNIV_MATRIX: {
            if (struct_id == "ISP_CCM_ATTR_S") {
                bIsCCMSumMember = utils::isMatchCCMSumTitleWithPrefix(path_str);
            }

            widget_obj.insert("type", "univ_matrix");
            QJsonObject param_obj;
            if (range_array.count() == 2 && range_array.at(0).isDouble() && range_array.at(1).isDouble()) {
                param_obj.insert("min", range_array.at(0));
                param_obj.insert("max", range_array.at(1));
            } else if ((range_array.count() > 2) && (range_array.count()%2 == 0)) {
                QJsonArray min_array, max_array;
                for (int i = 0; i < range_array.count(); i+=2) {
                    if (range_array.at(i).isDouble() && range_array.at(i+1).isDouble()) {
                        min_array.append(range_array.at(i));
                        max_array.append(range_array.at(i+1));
                    } else {
                        qDebug() << "[" + widget_obj["key"].toString() + "] Range definition is wrong.";
                        return -2;
                    }
                }

                param_obj.insert("min", min_array);
                param_obj.insert("max", max_array);
            } else {
                qDebug() << "[" + widget_obj["key"].toString() + "] Range definition is wrong.";
                return -2;
            }

            QJsonArray count_array;
            QJsonValue count_value = member_obj["COUNT"];
            if (count_value.isDouble()) {
                count_array.append(1);
                count_array.append(count_value.toInt());
            } else if (count_value.isArray()) {
                QJsonArray temp_array = count_value.toArray();
                int column_num = 0;
                if (temp_array.count() == 2) {
                    count_array.append(temp_array[0].toInt());
                    column_num = temp_array[1].toInt();
                    if (bIsCCMSumMember) {
                        column_num += 1;
                    }
                    count_array.append(column_num);
                } else if (temp_array.count() == 3) {
                    int n;
                    if (bIsNoiseCalCoef) {
                        n = temp_array[0].toInt();
                    } else {
                        n = temp_array[0].toInt() * temp_array[1].toInt() * temp_array[2].toInt();
                    }
                    count_array.append(1);
                    count_array.append(n);
                }
            }

            param_obj.insert("count", count_array);
            if (member_obj.contains("TITLE_H")) {
                QJsonArray title_array = member_obj["TITLE_H"].toArray();

                if (bIsCCMSumMember) {
                    title_array.append("SUM");
                }
                param_obj.insert("title_h", title_array);
            }

            if (member_obj.contains("TITLE_V"))
                param_obj.insert("title_v", member_obj["TITLE_V"].toArray());

            if (bIsNoiseCalCoef) {
                QStringList ISO_list = {"ISO100",
                                    "ISO200",
                                    "ISO400",
                                    "ISO800",
                                    "ISO1600",
                                    "ISO3200",
                                    "ISO6400",
                                    "ISO12800",
                                    "ISO25600",
                                    "ISO51200",
                                    "ISO102400",
                                    "ISO204800",
                                    "ISO409600",
                                    "ISO819200",
                                    "ISO1638400",
                                    "ISO3276800" };
                QJsonArray array = QJsonArray::fromStringList(ISO_list);
                param_obj.insert("title_h", array);
            }

            if (type_str == "CVI_FLOAT") {
                param_obj.insert("value_type", "double");
                widget_obj.insert("value_type", "double");
            }

            widget_obj.insert("param", param_obj);
            break;
        }
        default:
            return -3;
            break;
    }

    return 0;
}

