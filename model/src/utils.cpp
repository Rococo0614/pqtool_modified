#define D65_X 0.95047
#define D65_Y 1.0
#define D65_Z 1.08883

static double srgb_xyz[][3] = {
    {0.4124564,  0.3575761,  0.1804375},
    {0.2126729,  0.7151522,  0.0721750},
    {0.0193339,  0.1191920,  0.9503041}
};

static double Labf(double t)
{
    if (t > pow(6.0 / 29.0, 3.0)) {
        return pow(t, 1.0 / 3.0);
    }
    else {
        return 1.0 / 3.0 * pow(29.0 / 6.0, 2.0) * t + (4.0 / 29.0);
    }
}

static double Rgbf(double t)
{
    if (pow(t, 3.0) > pow(6.0 / 29.0, 3.0)) {
        return pow(t, 3.0);
    }
    else {
        return (t - 4.0/29.0)/(1.0 / 3.0 * pow(29.0 / 6.0, 2.0));
    }
}
static double srgb2linear(double srgb_val)
{
    double lin_val = 0;
    if (srgb_val < 0.04045) {
        lin_val = srgb_val / 12.92;
    } else {
        lin_val = exp(log((srgb_val + 0.055) / 1.055) * 2.4);
    }
    return lin_val;
}
static double linear2srgb(double lin_val)
{
    double srgb_val = 0;
    if (lin_val < 0.0031308) {
        srgb_val = lin_val * 12.92;
    } else {
        srgb_val = exp(log(1.055) + log(max(lin_val, 1e-3)) / 2.4) - 0.055;
    }
    return srgb_val;
}

void utils::RgbToLab(double red, double green, double blue, double* l, double* a, double* b)
{
    double x, y, z, re, gr, bl;

    re = srgb2linear(red / 255.0);
    gr = srgb2linear(green / 255.0);
    bl = srgb2linear(blue / 255.0);

    x = srgb_xyz[0][0] * re + srgb_xyz[0][1] * gr + srgb_xyz[0][2] * bl;
    y = srgb_xyz[1][0] * re + srgb_xyz[1][1] * gr + srgb_xyz[1][2] * bl;
    z = srgb_xyz[2][0] * re + srgb_xyz[2][1] * gr + srgb_xyz[2][2] * bl;

    x = x / D65_X;
    y = y / D65_Y;
    z = z / D65_Z;
    *l = 116.0 * Labf(y) - 16.0;
    *a = 500.0 * (Labf(x) - Labf(y));
    *b = 200.0 * (Labf(y) - Labf(z));
}

void utils::LabToRgb(double l, double a, double b, double *red, double *green, double *blue)
{
    double x, y, z;
    y = (l + 16.0)/116.0;
    x = a/500.0 + y;
    z = y - b/200.0;

    x = Rgbf(x) * D65_X;
    y = Rgbf(y) * D65_Y;
    z = Rgbf(z) * D65_Z;

    double t = srgb_xyz[0][0] * (srgb_xyz[1][1] * srgb_xyz[2][2] - srgb_xyz[2][1] * srgb_xyz[1][2])
            + srgb_xyz[0][1] * (srgb_xyz[2][0] * srgb_xyz[1][2] - srgb_xyz[1][0] * srgb_xyz[2][2])
            + srgb_xyz[0][2] * (srgb_xyz[1][0] * srgb_xyz[2][1] - srgb_xyz[2][0] * srgb_xyz[1][1]);
    double t1 = x * (srgb_xyz[1][1] * srgb_xyz[2][2] - srgb_xyz[2][1] * srgb_xyz[1][2])
            + y * (srgb_xyz[2][1] * srgb_xyz[0][2] - srgb_xyz[0][1] * srgb_xyz[2][2])
            + z * (srgb_xyz[0][1] * srgb_xyz[1][2] - srgb_xyz[1][1] * srgb_xyz[0][2]);
    double t2 = x * (srgb_xyz[2][0] * srgb_xyz[1][2] - srgb_xyz[1][0] * srgb_xyz[2][2])
            + y * (srgb_xyz[0][0] * srgb_xyz[2][2] - srgb_xyz[2][0] * srgb_xyz[0][2])
            + z * (srgb_xyz[1][0] * srgb_xyz[0][2] - srgb_xyz[0][0] * srgb_xyz[1][2]);
    double t3 = x * (srgb_xyz[1][0] * srgb_xyz[2][1] - srgb_xyz[2][0] * srgb_xyz[1][1])
            + y * (srgb_xyz[2][0] * srgb_xyz[0][1] - srgb_xyz[0][0] * srgb_xyz[2][1])
            + z * (srgb_xyz[0][0] * srgb_xyz[1][1] - srgb_xyz[0][1] * srgb_xyz[1][0]);

    *red = linear2srgb(t1 / t) * 255.0;
    *green = linear2srgb(t2 / t) * 255.0;
    *blue = linear2srgb(t3 / t) * 255.0;
}
