unique_ptr<int []> utils::readRawIntData(QString rawfilename, int width, int height, bool wdr_only_le)
{
    unsigned int size = static_cast<unsigned int>(width) * static_cast<unsigned int>(height) *
            static_cast<unsigned int>(sizeof(short));
    unique_ptr<int []> image(new int [size]);
    unsigned char *praw_data = reinterpret_cast<unsigned char *>(malloc(size));
    readRawFile(praw_data, size, rawfilename.toStdString().c_str());

    // convert to int layout
    int idx = 0;
    for (int r = 0; r < height; r ++ ){
        for (int c = 0; c < width; c ++ ){
            if (idx < static_cast<int>(size)) {
                unsigned char b1 = praw_data[idx];
                unsigned char b2 = praw_data[idx + 1];
                idx += 2;
                image.get()[(r * width) + c] = static_cast<int>(b2 << 8 | b1);
            }
        }
    }
    free(praw_data);

    if (!wdr_only_le) {
        return image;
    } else {
        unique_ptr<int []> image_wdr(new int [size / 2]);

        for (int r = 0; r < height; r ++ ){
            for (int c = 0; c < width / 2; c ++ ){
                image_wdr.get()[(r * width / 2) + c] = image.get()[r * width + c];
            }
        }
        return image_wdr;
    }
}

