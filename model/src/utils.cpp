QList<qreal> utils::interpolation(QList<qreal> values, int interpolation_number)
{
    QList<qreal> new_value;

    for (int i = 0; i < values.count() - 1; i++) {
        qreal v1 = values[i];
        qreal v2 = values[i + 1];
        qreal diff = (v2 - v1) / (interpolation_number);

        for (int j = 0; j < interpolation_number; j++) {
            qreal interpolation_value = v1 + j * diff;
            new_value.append(interpolation_value);
        }
    }

    return new_value;
}

