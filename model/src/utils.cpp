#define ZIP_FILE_HEADER_INFO_BTYE_1	(0x50)
#define ZIP_FILE_HEADER_INFO_BTYE_2	(0x4b)
#define ZIP_FILE_HEADER_INFO_BTYE_3	(0x03)
#define ZIP_FILE_HEADER_INFO_BTYE_4	(0x04)

#include <private/qzipreader_p.h>


bool utils::isZipFile(const QString &file_path, bool bExtract, QString &extract_path)
{
    QFile file(file_path);
    if (!file.open(QIODevice::ReadOnly)) {
        return false;
    }
    QByteArray data = file.readLine(5);
    file.close();

    if ((data.at(0) == ZIP_FILE_HEADER_INFO_BTYE_1) && (data.at(1) == ZIP_FILE_HEADER_INFO_BTYE_2)\
      && (data.at(2) == ZIP_FILE_HEADER_INFO_BTYE_3) && (data.at(3) == ZIP_FILE_HEADER_INFO_BTYE_4)) {

        if (bExtract) {
            if (extract_path.isNull()) {
                QFileInfo fileInfo = QFileInfo(file_path);
                QString parent_path = fileInfo.absolutePath();

                extract_path = parent_path + "/" + QString::number(QDateTime::currentDateTimeUtc().toTime_t(), 10) +"/";
            }

            QZipReader reader(file_path);
            QDir dir;

            if(!dir.exists(extract_path)) {
                dir.mkpath(extract_path);
            }

            reader.extractAll(extract_path);
            reader.close();
        }
        return true;
    }
    return false;
}

int utils::loadJson(const QString &file_path, QJsonObject &json, QString &error_msg, QString extract_file)
{
    QString json_string;
    QString file_path_tmp = file_path;
    int ret = 0;

    QString tmpDir = QString();
    isZipFile(file_path, true, tmpDir);

    if (!tmpDir.isNull()) {
        file_path_tmp = tmpDir + extract_file;
    }

    QFile file(file_path_tmp);
    if (!file.open(QIODevice::ReadOnly | QIODevice::Text)) {
        ret = -1;
        return ret;
    }
    json_string = file.readAll();
    file.close();

    QJsonParseError json_error;
    QJsonDocument json_doc = QJsonDocument::fromJson(json_string.toUtf8(),  &json_error);
    if ((json_doc.isNull()) || (json_error.error !=  QJsonParseError::NoError)) {
        error_msg = error_msg + "Failed to load json file: " + json_error.errorString();

        ret = -1;
        goto label;
    }

    json = json_doc.object();

label:
    if (!tmpDir.isNull()) {
        QDir dir;
        if (dir.exists(tmpDir)) {
            dir.setPath(tmpDir);
            dir.removeRecursively();
        }
    }

    return ret;
}

