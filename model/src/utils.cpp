                            QJsonArray points_array = curve_obj["POINTS"].toArray();
                            if (points_array.count() != 1) {
                                continue;
                            }

                            QStringList points_path_list;
                            QString y_path = points_array[0].toString();

                            if (!member_array.contains(y_path)) {
                                continue;
                            }

                            if (!all_contents_obj.contains(y_path)) {
                                continue;
                            }

                            QJsonObject y_obj = all_contents_obj[y_path].toObject();
                            QJsonObject y_param_obj = y_obj["param"].toObject();
                            QJsonArray y_count_array = y_param_obj["count"].toArray();
                            if ((y_count_array[0].toInt() != 1) || (y_count_array[1].toInt() <= 1))
                            {
                                continue;
                            }
                            points_path_list.append(y_path);

                            QJsonObject univ_curve_obj;
                            QJsonArray key_array;
                            QJsonObject param_obj;
                            QJsonArray min_array;
                            QJsonArray max_array;
                            QJsonArray title_v_array;

                            QJsonObject content_obj = all_contents_obj[y_path].toObject();
                            QJsonObject temp_obj = content_obj["param"].toObject();
                            QJsonArray ori_count_array = temp_obj["count"].toArray();
                            min_array.append(temp_obj["min"].toInt());
