void utils::dpcm_tx(const ushort *idata, ushort *odata, int width, int height, int mode)
{
    ushort xpred, xenco, xdeco, pix_buf[4];
	bool nop;
	int reg_img_height = height;
	int reg_img_width = width;

    ushort (*predictor)(ushort , ushort *, bool *) = NULL;
    ushort (*encoder)(bool , ushort , ushort ) = NULL;
    ushort (*decoder)(bool , ushort , ushort ) = NULL;

	switch(mode)
	{
	case 4  : encoder = encode_12_10_12; decoder = decode_12_10_12; predictor = predictor1; break;
    case 5  : encoder = encode_12_8_12 ; decoder = decode_12_8_12 ; predictor = predictor1; break;
    case 6  : encoder = encode_12_7_12 ; decoder = decode_12_7_12 ; predictor = predictor2; break;
    case 7  : encoder = encode_12_6_12 ; decoder = decode_12_6_12 ; predictor = predictor2; break;
	}

	for (int y = 0; y < reg_img_height; y++) {
		for (int x = 0; x < reg_img_width; x++) {
			int idx = y * reg_img_width + x;
			if(mode == 0) // bypass
				odata[idx] = idata[idx];
			else{
				xpred = predictor(x, pix_buf, &nop);
				xenco = encoder(nop, idata[idx], xpred);
				xdeco = decoder(nop, xenco, xpred);
				odata[idx] = xenco;

				// buffer shift
				pix_buf[3] = pix_buf[2];
				pix_buf[2] = pix_buf[1];
				pix_buf[1] = pix_buf[0];
				pix_buf[0] = xdeco;
			}
		}
	}
}

