unsigned char *utils::convertAnyRaw2Raw16(const unsigned char *praw_data, unsigned int width, unsigned height,
        RAW_BITS bits, RAW_PACK_TYPE raw_pack_type)
{
    unsigned int unpacked_raw_size = width * height * sizeof(short);
    unsigned char *unpacked_raw_data = reinterpret_cast<unsigned char *>(malloc(unpacked_raw_size));

    if (!unpacked_raw_data) {
        return nullptr;
    }

    if (bits == RAW_BITS::BITS_8) {
        for (unsigned int  i = 0; i < width * height; i++) {
            unpacked_raw_data[i] = praw_data[i];
        }
    } else if (bits == RAW_BITS::BITS_16) {
        memcpy(unpacked_raw_data, praw_data, unpacked_raw_size);
    } else if (raw_pack_type == RAW_PACK_TYPE::RAW_PACKED) {
        raw_packed2unpacked(praw_data, reinterpret_cast<unsigned short *>(unpacked_raw_data), width, height);
    } else if (raw_pack_type == RAW_PACK_TYPE::RAW_DPCM6) {
        QByteArray src_6b, src_12b;
        int v0, v1, v2;
        int temp[4];
        const unsigned char *p = praw_data;
        char c = 0, zero = 0;
        for (unsigned int i = 0; i < width * height / 4; i++) {
            v0 = *p++;
            v1 = *p++;
            v2 = *p++;
            temp[0] = static_cast<unsigned short>(((v0 & 0x03) << 4) | (v2 & 0x0F ));
            temp[1] = static_cast<unsigned short>((v0 & 0xFC) >> 2);
            temp[2] = static_cast<unsigned short>(((v1 & 0x03) << 4) | ((v2 & 0xF0) >> 4));
            temp[3] = static_cast<unsigned short>((v1 & 0xFC) >> 2);
            c = temp[0] & 0xFF;
            src_6b.append(temp[0] & 0xFF);
            src_6b.append(zero);
            c = temp[1] & 0xFF;
            src_6b.append(c);
            src_6b.append(zero);
            c = temp[2] & 0xFF;
            src_6b.append(c);
            src_6b.append(zero);
            c = temp[3] & 0xFF;
            src_6b.append(c);
            src_6b.append(zero);
        }
        src_12b.resize(width * height * 2);
        utils::dpcm_rx((unsigned short *)src_6b.data(), (unsigned short *)src_12b.data(), width, height, 7);
        memcpy((void *)unpacked_raw_data, (void *)src_12b.constData(), unpacked_raw_size);
    } else {
        memcpy(unpacked_raw_data, praw_data, unpacked_raw_size);
    }

    return unpacked_raw_data;
}

